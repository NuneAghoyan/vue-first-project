<script>
import Product from './components/Product.vue';
export default {
  components: {
    Product
  },
  data() {
    return {
      fruits: [
        {
          name: 'apple',
          price: '3$',
          description: 'Golden apples from Armenia'
        },
        {
          name: 'pear',
          price: '3.4$',
          description: 'Sweet pears'
        },
        {
          name: 'cherry',
          price: '2.6$',
          description: 'Sour red cherries'
        },
        {
          name: 'lemon',
          price: '4$',
          description: 'Big Chinese lemons'
        },
        {
          name: 'banana',
          price: '3.7$',
          description: 'Fresh bananas from Ecuador'
        },
      ]
    };
  },
  methods: {
    onChangeCurrency(index) {
      let fruit = this.fruits[index];
      if (fruit.price[fruit.price.length - 1] === "$") {
        fruit.price = parseFloat(fruit.price) * 400 + "÷è";
      } else {
        fruit.price = parseFloat(fruit.price) / 400 + "$";
      }
    }
  }
}
</script>

<template>
  <main>
    <Product v-for="(fruit, index) in fruits" :key="fruit.name" :fruit="fruit"
      @changeCurrency="onChangeCurrency(index)" />
  </main>
</template>
